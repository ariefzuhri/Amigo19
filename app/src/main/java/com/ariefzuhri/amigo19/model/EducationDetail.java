package com.ariefzuhri.amigo19.model;

public class EducationDetail {
    private int image;
    private String content;

    public EducationDetail(int image, String content) {
        this.image = image;
        this.content = content;
    }

    public int getImage() {
        return image;
    }

    public String getContent() {
        return content;
    }
}
